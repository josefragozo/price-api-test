CREATE TABLE IF NOT EXISTS productPrice (
    ID int NOT NULL AUTO_INCREMENT PRIMARY KEY,
    BRAND_ID int,
    START_DATE timestamp,
    END_DATE timestamp,
    PRICE_LIST int,
    PRODUCT_ID int,
    PRIORITY int,
    PRICE DOUBLE PRECISION,
    CURR varchar(20),
    CONSTRAINT FK_BRAND_ID foreign key (BRAND_ID) references brand(ID)
);

